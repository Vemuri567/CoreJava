package Constructor;

public class Constructors {
    int a,b;
    public Constructors(int a,int b){
        this.a=a;
        this.b=b;
    }
    public int addition(){
        int c=a+b;
        return c;

    }
    public int substraction(){
        int c=a-b;
        return c;
    }
    public int multiplication(){
        int c=a*b;
        return c;

    }


}
